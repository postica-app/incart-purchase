import { CreateOrderType } from 'incart-fe-common'
import { cfetch } from '@/functions'

export default {
    async createOrder(props: CreateOrderType) {
        const orderResult = await (
            await cfetch('order', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                },
                body: JSON.stringify(props),
            })
        ).text()

        return orderResult
    },
}
